function letrascont(countries) {
  let letrasCountries = [
    ...new Set(
      Object.values(
        countries.map((paises) => {
          return paises.charAt(0).toUpperCase();
        })
      )
    ),
  ];

  // Crear un objeto para contar las letras
  let contador = {};

  // Inicializar el contador para cada letra
  letrasCountries.forEach((letra) => {
    contador[letra] = 0;
  });
  // Contar las letras al inicio de cada país
  countries.forEach((pais) => {
    // Tomar la primera letra del país
    let primeraLetra = pais.charAt(0).toUpperCase();

    // Si la primera letra está en letras, incrementar el contador
    if (contador.hasOwnProperty(primeraLetra)) {
      contador[primeraLetra]++;
    }
  });
  let letrasPaises = letrasCountries.map((letra) => ({
    letra: letra,
    cantidad: contador[letra],
  }));
 
  let paises = Object.values(letrasPaises);
  console.log(paises)
  return paises.reduce((acumulador, cantidadLetra) => {
    return cantidadLetra.cantidad > acumulador.cantidad ? cantidadLetra : acumulador;
  }, paises[0]);
}
